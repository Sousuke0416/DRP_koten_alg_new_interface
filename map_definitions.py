# =============================================================================
# map_definitions.py
# マップ定義ファイル
#
# このファイルにシミュレーションで使いたいマップを定義する。
# 定義したマップは EnvConfig(map_data=MAP_XXX, ...) の形で渡す。
#
# =============================================================================
#
# 【データ形式の解説】
#
# マップは Python の辞書（dict）で表現する。
# キーは "nodes" と "edges" の 2 つ。
#
# {
#     "nodes": [ ... ],   # ノード（頂点）のリスト
#     "edges": [ ... ],   # エッジ（辺）のリスト
# }
#
# ─────────────────────────────────────────────────────────────────────────────
# ■ nodes（ノードリスト）
#
#   各ノードは以下のキーを持つ辞書で定義する。
#
#   {
#       "id": 0,       # ノードID（int）。0 以上の整数。リスト内で一意であること。
#       "x":  0.0,     # X 座標（float）。描画・衝突判定の連続座標計算に使用。
#       "y":  0.0,     # Y 座標（float）。同上。
#   }
#
#   ※ x, y は実際の距離スケールと合わせておくと衝突判定が直感的になる。
#      エッジ重み = 2 点間のユークリッド距離 にしておくのが推奨。
#
# ─────────────────────────────────────────────────────────────────────────────
# ■ edges（エッジリスト）
#
#   各エッジは以下のキーを持つ辞書で定義する。
#   グラフは「無向」なので u→v と v→u の両方向に通行できる。
#
#   {
#       "u":      0,    # 始点ノードID（int）
#       "v":      1,    # 終点ノードID（int）
#       "weight": 10.0, # エッジの重み（float, 正の値）
#                       # = 物理的な距離（移動距離）を表す。
#                       # エージェントの speed=5 なので weight=10 なら 2 step で通過。
#   }
#
#   ※ weight は必ず正の値にすること（0 や負は不可）。
#   ※ 自己ループ（u == v）は定義しないこと。
#
# ─────────────────────────────────────────────────────────────────────────────
# ■ ノード座標とエッジ重みの関係（推奨）
#
#   物理的に正確なマップにしたい場合は
#       weight = sqrt((x1-x0)^2 + (y1-y0)^2)
#   とする。視覚的にわかりやすい簡略マップの場合は任意の値で構わない。
#
# ─────────────────────────────────────────────────────────────────────────────
# ■ エージェント設定との対応
#
#   from env.mapf_env import EnvConfig, AgentConfig
#
#   config = EnvConfig(
#       map_data      = MAP_GRID_4x4,        # ← このファイルで定義したマップを渡す
#       agent_configs = [
#           AgentConfig(agent_id=0, start_node=0, goal_node=15),
#           AgentConfig(agent_id=1, start_node=3, goal_node=12),
#       ],
#       max_steps = 500,
#   )
#
# =============================================================================


